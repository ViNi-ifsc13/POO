package ads.poo;

public class Retangulo {

    // Atributos
        private int largura;
        private int altura;
        private String cod;


    // Métodos construtor

    public Retangulo() {
        this.largura = 4;
        this.altura = 3;
        this.cod = "ASCII";
    }

    public Retangulo(int largura, int altura, String cod) {
        this.largura = largura;
        this.altura = altura;
        this.cod = cod;
    }

    // Métodos


}
